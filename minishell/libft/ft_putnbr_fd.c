#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	u;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			n = -n;
		}
		u = n % 10;
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
		}
		ft_putchar_fd(u + '0', fd);
	}
}
